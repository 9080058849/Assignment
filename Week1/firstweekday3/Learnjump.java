package firstweek3day;

public class Learnjump {
public static void main(String[] args) {
	for(int i=1;i<=10;i++)
	{
		if(i==7)
		{
			System.out.println("Seven"); 
			continue;
/*(it is a jump statement output:1,2,three,3,4,5 to skip the 3 in number we have use the jump statement(continue)*/
		}
	System.out.println(i);
    }
  }
}
